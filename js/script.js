let previousPageButton = document.querySelector('.previous-page-button');
let nextPageButton = document.querySelector('.next-page-button');
let lastPageButton = document.querySelector('.last-page-button');
let firstPageButton = document.querySelector('.first-page-button');
let page = document.querySelector('.page');
let blurredPage = document.querySelector('.blurred-page');
let pageCounter = document.querySelector('.page-number');
let releaseDate = document.querySelector('.release-date');
let sketchVerButton = document.querySelector('.sketch-ver-button');
let speschlessVerButton = document.querySelector('.speschless-ver-button');
let goToButton = document.querySelector('.go-to-page-button');
let pageNumber = 0;
let lastPageNumber = full_urls.length;

let update = function (updPageNumber) {
  page.src = full_urls[updPageNumber];
  blurredPage.src = full_urls[updPageNumber];
  pageCounter.textContent = updPageNumber + 1 + '/' + lastPageNumber;
  let rawDate = full_urls[updPageNumber].slice(41, 49);
  let formattedDate = `${rawDate.slice(0, 4)}/${rawDate.slice(4, 6)}/${rawDate.slice(6, 8)}`;
  releaseDate.textContent = formattedDate;
  sketchVerButton.href = 'https://twokindscomic.com/images/'+full_urls[updPageNumber].slice(41, 49)+'_sketch.png';
  document.querySelector('.speschless-ver-button').href = 'https://twokindscomic.com/speechless/?p='+updPageNumber;
}

update(pageNumber);

previousPageButton.addEventListener('click', function (evt) {
  evt.preventDefault();
  if (!pageNumber == 0) {
    pageNumber = pageNumber - 1;
    update(pageNumber);
  }
});

nextPageButton.addEventListener('click', function (evt) {
  evt.preventDefault();
  if (pageNumber < lastPageNumber) {
    pageNumber = pageNumber + 1;
    update(pageNumber);
  }
});

lastPageButton.addEventListener('click', function (evt) {
  evt.preventDefault();
  pageNumber = full_urls.length - 1;
  update(pageNumber);
});

firstPageButton.addEventListener('click', function (evt) {
  evt.preventDefault();
  pageNumber = 0;
  update(pageNumber);
});

goToButton.addEventListener('click', function (evt) {
  evt.preventDefault();
  let pageInput = document.querySelector('.page-input').valueAsNumber;
  pageNumber = pageInput;
  if (pageInput >= 1 && pageInput <= lastPageNumber + 1) {
    document.querySelector('.error').classList.add('hidden');
    update(pageInput - 1);
  } else {
    document.querySelector('.error').classList.remove('hidden');
  }
  document.querySelector('.page-input').value = "";
});